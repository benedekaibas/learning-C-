#include <iostream>
using namespace std;

int main() {
    int integer_value = 4;
    bool boolean_value = true;
    string string_value = "ABC";
    char char_value = 'A';
    double double_value = 87.43;

    cout << "Integer: " << integer_value << endl;
    cout << "Boolean: " << boolean_value << endl;
    cout << "String: " << string_value << endl;
    cout << "Character: " << char_value << endl;
    cout << "Double: " << double_value << endl;

    return 0;
}