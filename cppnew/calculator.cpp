/*
Write a simple calculator in c++
*/

#include <iostream>
using namespace std;

int add(number_one, number_two){

    return number_one + number_two;
};

int substract(number_one, number_two){

    return number_one - number_two;
}

int multiply(number_one, number_two){

    return number_one * number_two;
}

int divide(number_one, number_two){

    return number_one / number_two;
}